setup_aider_global() {
    info "Setting up Aider (global) → ~/.aider.conf.yml"

    local aider_dir="$HOME/.aider"
    mkdir -p "$aider_dir"

    cat > "$aider_dir/CONVENTIONS.md" << CONV_EOF
# Coding Conventions (Arche)

> Auto-generated by arche framework. Source: $REPO_DIR/

## Agent Roles
When operating in a specific role, read the definition from $REPO_DIR/agents/<role>.md.
Available: archon, polemarch, demiourgos, symboulos, tamias, phylax, thesmothete, strategos, epistates, grammateus, taxiarch, mnemon, skopos, technites, constitution, regulation, process, quality, architecture, security, knowledge, ethics, performance, continuity, evolution, coherence, diabolos.

## Constitutional Law
### Session Start (MANDATORY)
1. Read **global governance state**: \`\$HOME/.arche/governance.md\`
2. Read **workspace state**: \`state.md\` (if it exists in the project root)

### Rules
- **Before work**: Search ~/.arche/knowledge/ for existing knowledge on the topic
- **After work**: Write transferable findings to ~/.arche/knowledge/ per Regulation 5 format
- Communication: Agent artifacts in English; user-facing in user's language
- Model allocation: Strongest for strategy/review, fastest for execution

## Quality Standards
- 0 warnings, 0 errors on every build
- No warning suppression — fix in source code
- Clean build before verification

## Workflow
- Deploy and verify after changes
- Knowledge articles must include reasoning chains, not just conclusions
CONV_EOF
    log "Generated ~/.aider/CONVENTIONS.md"

    if [ -f "$AIDER_GLOBAL_CONF" ]; then
        if grep -q 'CONVENTIONS.md' "$AIDER_GLOBAL_CONF" 2>/dev/null; then
            log "~/.aider.conf.yml already references CONVENTIONS.md"
        else
            warn "~/.aider.conf.yml exists but doesn't reference CONVENTIONS.md"
            echo "  Add this to ~/.aider.conf.yml:"
            echo "  read: [$aider_dir/CONVENTIONS.md]"
        fi
    else
        cat > "$AIDER_GLOBAL_CONF" << EOF
# Arche Aider global configuration
read:
  - $aider_dir/CONVENTIONS.md
EOF
        log "Generated ~/.aider.conf.yml"
    fi

    log "Aider global setup complete"
}

