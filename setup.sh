Arche Framework Setup v${VERSION}

Usage:
  ./setup.sh <tool> [<tool> ...]    Set up specified tools (auto-detects scope)
  ./setup.sh init <tool|all>        Clean existing config, then set up fresh
  ./setup.sh update <tool>          Re-sync global config from source
  ./setup.sh update knowledge       Merge new seed articles into ~/.arche/knowledge/
  ./setup.sh sync-all               Re-sync global + all registered workspaces (symlink mode)
  ./setup.sh doctor                 Check symlink health across all installations
  ./setup.sh status                 Show installation status
  ./setup.sh -h | --help            Show this help

Tools:
  cursor    Cursor IDE
  claude    Claude Code
  roo       Roo Code
  kilo      Kilo Code
  aider     Aider
  codex     OpenAI Codex CLI     (workspace only)
  gemini    Gemini CLI           (workspace only)
  cline     Cline                (workspace only)
  copilot   GitHub Copilot       (workspace only)
  windsurf  Windsurf             (workspace only)
  agents-md AGENTS.md standard   (workspace only)
  all       All applicable tools
  workspace All workspace-only tools

Behavior:
  From ~/  (or non-project dir) → installs global config only
  From a project directory      → installs workspace config
                                  (+ global if not yet installed)

Strategy:
  agents/ and rules/ are SYMLINKED to source (auto-follow on source edits)
  knowledge/ is SYMLINKED to ~/.arche/knowledge/ (bidirectional, agents write back)
  governance.md is SYMLINKED to ~/.arche/governance.md (global state)
  Cursor rules/ are COPIED with .mdc conversion (Cursor format requirement)

Examples:
  ./setup.sh <tool> [tool...]     # From ~/ → global only
  cd myproject && ~/arche/setup.sh <tool>  # workspace + global if needed
  ./setup.sh all                  # Everything applicable to current context
  ./setup.sh init <tool>          # Clean old files, then fresh install
  ./setup.sh init all             # Clean + fresh install for all global tools
  ./setup.sh update <tool>        # Re-sync global config
  ./setup.sh update knowledge     # Add new seed articles (no overwrite)
  ./setup.sh sync-all             # Re-sync global + all registered workspaces
  ./setup.sh doctor               # Check symlink health
  ./setup.sh status               # Show what's installed
USAGE
}

