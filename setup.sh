_check_global() {
    local name="$1" dir="$2"
    if [ -d "$dir/agents" ] || [ -d "$dir/rules" ]; then
        echo -e "  ${GREEN}✓${NC} $name ($dir)"
        [ -d "$dir/agents" ] && echo "    agents: $(ls "$dir/agents/" 2>/dev/null | wc -l) files"
        [ -d "$dir/rules" ] && echo "    rules:  $(ls "$dir/rules/" 2>/dev/null | wc -l) files"
        if [ -L "$dir/knowledge" ]; then
            echo "    knowledge: linked → $(readlink "$dir/knowledge")"
        elif [ -d "$dir/knowledge" ]; then
            echo "    knowledge: directory ($(find "$dir/knowledge" -name '*.md' 2>/dev/null | wc -l) articles)"
        else
            echo "    knowledge: not installed"
        fi
    else
        echo -e "  ${RED}✗${NC} $name — not installed"
    fi
}

cmd_status() {
    echo "Arche Framework Status v${VERSION}"
    echo "===================================="
    echo "Source: $REPO_DIR"
    echo ""

    echo "Global installations:"
    _check_global "Cursor" "$CURSOR_DIR"
    _check_global "Claude Code" "$CLAUDE_DIR"
    _check_global "Roo Code" "$ROO_DIR"
    _check_global "Kilo Code" "$KILO_DIR"

    if [ -f "$AIDER_GLOBAL_CONF" ]; then
        echo -e "  ${GREEN}✓${NC} Aider (~/.aider.conf.yml)"
        [ -f "$HOME/.aider/CONVENTIONS.md" ] && echo "    conventions: present" || echo "    conventions: missing"
    else
        echo -e "  ${RED}✗${NC} Aider — not installed"
    fi

    echo ""
    echo "Workspace files (current directory: $(pwd)):"
    for f in AGENTS.md GEMINI.md CONVENTIONS.md .clinerules .windsurfrules .github/copilot-instructions.md CLAUDE.md .aider.conf.yml .gemini/settings.json; do
        if [ -f "./$f" ]; then
            echo -e "  ${GREEN}✓${NC} $f"
        else
            echo -e "  ${YELLOW}·${NC} $f — not present"
        fi
    done

    echo ""
    echo "Context: $(pwd)"
    if is_workspace; then
        echo "  Mode: workspace (smart setup will install workspace + global if needed)"
    else
        echo "  Mode: global-only (smart setup will install global config)"
    fi
}

cmd_update() {
    local target="$1"
    info "Updating $target from source..."
    case "$target" in
        cursor)  setup_cursor_global ;;
        claude)  setup_claude_global ;;
        roo)     setup_roo_global ;;
        kilo)    setup_kilo_global ;;
        aider)   setup_aider_global ;;
        knowledge)
            ensure_knowledge
            local added=0
            while IFS= read -r -d '' src; do
                local rel="${src#$REPO_DIR/knowledge/}"
                local dst="$ARCHE_STATE_DIR/knowledge/$rel"
                if [ ! -f "$dst" ]; then
                    mkdir -p "$(dirname "$dst")"
                    cp "$src" "$dst"
                    log "Added new article: $rel"
                    added=$((added + 1))
                fi
            done < <(find "$REPO_DIR/knowledge" -name '*.md' -print0)
            if [ "$added" -eq 0 ]; then
                info "No new articles to add (all seed articles already exist in ~/.arche/knowledge/)"
            else
                log "Added $added new article(s) to ~/.arche/knowledge/"
            fi
            ;;
        *)       err "Update works for: cursor, claude, roo, kilo, aider, knowledge"; exit 1 ;;
    esac
}

cmd_init() {
    local target="$1"
    info "Initializing $target (clean + setup)..."
    case "$target" in
        cursor)  clean_cursor; setup_cursor_smart ;;
        claude)  clean_claude; setup_claude_smart ;;
        roo)     clean_roo;    setup_roo_smart ;;
        kilo)    clean_kilo;   setup_kilo_smart ;;
        aider)   clean_aider;  setup_aider_smart ;;
        all)
            for t in cursor claude roo kilo aider; do
                cmd_init "$t"
                echo ""
            done
            return
            ;;
        *)       err "Init works for: cursor, claude, roo, kilo, aider, all"; exit 1 ;;
    esac
    log "$target initialized"
}

