RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

log()  { echo -e "${GREEN}[OK]${NC} $*"; }
warn() { echo -e "${YELLOW}[WARN]${NC} $*"; }
err()  { echo -e "${RED}[ERR]${NC} $*" >&2; }
info() { echo -e "${BLUE}[INFO]${NC} $*"; }

# ==================== Helpers ====================

copy_dir() {
    local src="$1" dst="$2" label="$3"
    if [ -L "$dst" ]; then
        info "Removing old symlink: $dst"
        rm "$dst"
    fi
    mkdir -p "$dst"
    cp -r "$src"/* "$dst"/ 2>/dev/null || true
    log "Copied $label → $dst"
}

clean_dir() {
    local dst="$1" label="$2"
    if [ -L "$dst" ]; then
        rm "$dst"
        info "Removed symlink: $dst ($label)"
    elif [ -d "$dst" ]; then
        rm -rf "$dst"
        info "Removed directory: $dst ($label)"
    fi
}

link_dir() {
    local src="$1" dst="$2" label="$3"
    if [ -L "$dst" ]; then
        rm "$dst"
    elif [ -d "$dst" ]; then
        warn "$dst exists as directory. Backing up to ${dst}.bak"
        mv "$dst" "${dst}.bak"
    fi
    ln -sfn "$src" "$dst"
    log "Linked $label → $dst"
}

# Ensure workspace-local real directory (for workspace-specific knowledge).
# If dst is a symlink (legacy setup), remove it — workspace-specific knowledge
# must NOT flow to global. Preserve existing directory contents.
ensure_local_dir() {
    local dst="$1" label="$2"
    if [ -L "$dst" ]; then
        rm "$dst"
        info "Removed legacy symlink: $dst (converting to workspace-local real directory)"
    fi
    if [ ! -d "$dst" ]; then
        mkdir -p "$dst"
        log "Created workspace-local $label → $dst"
    else
        info "$label already exists as directory: $dst (preserved)"
    fi
}

is_workspace() {
    [ "$(pwd)" != "$HOME" ] && [ "$(pwd)" != "$REPO_DIR" ]
}

has_global() {
    local dir="$1"
    [ -d "$dir/agents" ] || [ -d "$dir/rules" ]
}

