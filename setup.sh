        cp "$agent" "$KILO_DIR/rules/agent-${base}"
    done
    log "Copied agent definitions as rules → $KILO_DIR/rules/"

    for rule in "$REPO_DIR"/rules/*.md; do
        [ -f "$rule" ] || continue
        cp "$rule" "$KILO_DIR/rules/$(basename "$rule")"
    done
    log "Copied rules → $KILO_DIR/rules/"

    link_dir "$ARCHE_STATE_DIR/knowledge" "$KILO_DIR/knowledge" "knowledge"
    ln -sfn "$ARCHE_STATE_DIR/governance.md" "$KILO_DIR/governance.md"

    log "Kilo Code global setup complete"
}

