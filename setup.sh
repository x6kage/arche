register_workspace() {
    local ws="$1"
    local registry="$ARCHE_STATE_DIR/workspaces.list"
    mkdir -p "$ARCHE_STATE_DIR"
    touch "$registry"
    if ! grep -Fxq "$ws" "$registry" 2>/dev/null; then
        echo "$ws" >> "$registry"
        info "Registered workspace → $registry"
    fi
}

list_workspaces() {
    local registry="$ARCHE_STATE_DIR/workspaces.list"
    [ -f "$registry" ] || return 0
    grep -v '^[[:space:]]*$' "$registry" 2>/dev/null || true
}

cmd_sync_all() {
    local registry="$ARCHE_STATE_DIR/workspaces.list"
    info "Syncing all registered workspaces..."

    info "→ Global sync first (cursor, claude, roo, kilo)"
    for t in cursor claude roo kilo; do
        cmd_update "$t" 2>/dev/null || warn "$t global update skipped (not installed)"
    done

    if [ ! -f "$registry" ]; then
        warn "No workspace registry at $registry — nothing to sync"
        return 0
    fi

    local count=0 missing=0 failed=0
    while IFS= read -r ws; do
        [ -z "$ws" ] && continue
        count=$((count + 1))
        if [ ! -d "$ws" ]; then
            warn "Workspace missing, skipping: $ws"
            missing=$((missing + 1))
            continue
        fi
        info "→ Syncing workspace: $ws"
        (
            cd "$ws" || exit 1
            # Claude workspace setup (symlink migration for .claude/)
            if [ -d "./.claude" ]; then
                setup_claude_smart
            fi
            # Cursor workspace setup (.mdc conversion)
            if [ -d "./.cursor" ]; then
                setup_cursor_smart
            fi
            # Roo / Kilo workspace setup
            if [ -d "./.roo" ]; then
                setup_roo_smart
            fi
            if [ -d "./.kilo" ]; then
                setup_kilo_smart
            fi
            # Generic workspace tools (AGENTS.md, Gemini, Cline, Copilot, Windsurf, Aider)
            setup_workspace
        ) || {
            err "Workspace sync failed: $ws"
            failed=$((failed + 1))
        }
    done < "$registry"

    echo ""
    log "sync-all summary: $count registered, $missing missing, $failed failed"
}

cmd_doctor() {
    info "Checking Arche symlink health..."
    local issues=0

    local check_paths=(
        "$HOME/.cursor/knowledge"
        "$HOME/.cursor/governance.md"
        "$HOME/.claude/agents"
        "$HOME/.claude/rules"
        "$HOME/.claude/knowledge"
        "$HOME/.claude/governance.md"
        "$HOME/.roo/knowledge"
        "$HOME/.kilo/knowledge"
    )

    for p in "${check_paths[@]}"; do
        if [ -L "$p" ]; then
            if [ -e "$p" ]; then
                local target
                target=$(readlink "$p")
                log "OK symlink: $p → $target"
            else
                err "BROKEN symlink: $p → $(readlink "$p")"
                issues=$((issues + 1))
            fi
        elif [ -e "$p" ]; then
            warn "NOT symlink (copy/dir): $p — consider 'arche sync-all' to migrate"
        else
            info "MISSING: $p (tool not installed)"
        fi
    done

    echo ""
    local registry="$ARCHE_STATE_DIR/workspaces.list"
    if [ -f "$registry" ]; then
        info "Checking registered workspaces..."
        while IFS= read -r ws; do
            [ -z "$ws" ] && continue
            if [ ! -d "$ws" ]; then
                warn "Registered workspace missing: $ws"
                issues=$((issues + 1))
                continue
            fi
            for sub in agents rules governance.md; do
                local p="$ws/.claude/$sub"
                [ -e "$p" ] || continue
                if [ -L "$p" ]; then
                    if [ -e "$p" ]; then
                        log "OK workspace link: $p"
                    else
                        err "BROKEN workspace symlink: $p"
                        issues=$((issues + 1))
                    fi
                else
                    warn "NOT symlink: $p — run 'cd $ws && arche claude'"
                fi
            done

            # workspace-local knowledge dirs (MUST be real directories, never symlinks).
            # A symlink here means legacy setup where workspace knowledge drained into global.
            for tool_dir in .claude .cursor .roo .kilo; do
                local kp="$ws/$tool_dir/knowledge"
                [ -e "$kp" ] || continue
                if [ -L "$kp" ]; then
                    err "LEGACY symlink (workspace knowledge must be real dir): $kp — run 'cd $ws && arche claude' to migrate"
                    issues=$((issues + 1))
                elif [ -d "$kp" ]; then
                    log "OK workspace-local knowledge dir: $kp"
                else
                    warn "NOT a directory: $kp"
                    issues=$((issues + 1))
                fi
            done
        done < "$registry"
    else
        info "No workspace registry yet (normal for fresh install)"
    fi

    echo ""
    if [ "$issues" -eq 0 ]; then
        log "Doctor: no issues detected"
    else
        err "Doctor: $issues issue(s) detected"
        return 1
    fi
}

